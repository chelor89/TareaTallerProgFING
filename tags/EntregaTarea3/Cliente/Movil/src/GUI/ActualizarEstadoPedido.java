    private void tblPedidosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblPedidosMouseClicked
        DefaultTableModel tablaP = (DefaultTableModel) tblPedidos.getModel();
        String estado = (tablaP.getValueAt(tblPedidos.getSelectedRow(), 1)).toString();
        pedidoSeleccionado = (int) (tablaP.getValueAt(tblPedidos.getSelectedRow(), 0));
        switch (estado) {
            case "PREPARACION":
                rbtPreparacion.setSelected(true);
                rbtPreparacion.setEnabled(false);
                rbtEnviado.setEnabled(true);
                rbtRecibido.setEnabled(true);
                btnActualizar.setEnabled(false);
                break;
            case "ENVIADO":
                rbtEnviado.setSelected(true);
                rbtPreparacion.setEnabled(false);
                rbtEnviado.setEnabled(false);
                rbtRecibido.setEnabled(true);
                btnActualizar.setEnabled(false);
                break;
            default:
                rbtRecibido.setSelected(true);
                rbtPreparacion.setEnabled(false);
                rbtEnviado.setEnabled(false);
                rbtRecibido.setEnabled(false);
                btnActualizar.setEnabled(false);
                break;
        }


    }//GEN-LAST:event_tblPedidosMouseClicked

    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        Estado e;
        if (bg.getSelection().isEnabled()) {
            if (bg.getSelection().equals(rbtRecibido.getModel())) {
                e = Estado.RECIBIDO;
            } else if (bg.getSelection().equals(rbtEnviado.getModel())) {
                e = Estado.ENVIADO;
            } else {
                e = Estado.PREPARACION;
            }

            port.actualizarEstadoPedido(pedidoSeleccionado, e, null);
            JOptionPane.showMessageDialog(null, "El estado del pedido ha sido actualizado con √©xito.");
            btnActualizar.setEnabled(false);
            bg.clearSelection();
            rbtPreparacion.setEnabled(false);
            rbtEnviado.setEnabled(false);
            rbtRecibido.setEnabled(false);
            LlenarTablaPedidos();
        }


    }//GEN-LAST:event_btnActualizarActionPerformed

