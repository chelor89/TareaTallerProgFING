    private void LlenarTablaPedidos() {
        DefaultTableModel tablaP = (DefaultTableModel) tblPedidos.getModel();
        tablaP.setRowCount(0);
        try{
            List<DataPedido> pedidos = port.getPedidos().getItem();

            for (DataPedido dp : pedidos) {

                String fecha = dp.getFecha().getDia() + "/" + dp.getFecha().getMes() + "/" + dp.getFecha().getAnio();
                if (dp.getFecha().getHora() != -1) {
                    fecha = fecha + "-" + dp.getFecha().getHora() + ":" + dp.getFecha().getMinuto();
                }
                tablaP.addRow(new Object[]{dp.getIdP(), dp.getEstado().toString(), dp.getNickCli(), fecha});
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "Sin conexion");
        }    
    }


