    private void tablaProdsMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaProdsMouseClicked
         dpActual = cr.seleccionarProducto((String) tablaProds.getValueAt(tablaProds.getSelectedRow(), 0),
                 (String) tablaProds.getValueAt(tablaProds.getSelectedRow(), 2));

         if (dpActual.getTipo() == Producto.Individual) {
              textNombre.setEnabled(true);
              textDescripcion.setEnabled(true);
              textPrecio.setEnabled(true);
//              textImagen.setEnabled(true);
              textDescuento.setEnabled(false);
              comboBoxAct.setEnabled(false);

              textDescuento.setText("");
              textNombre.setText(dpActual.getNombre());
              textDescripcion.setText(dpActual.getDescripcion());
              textPrecio.setText(Integer.toString(dpActual.getPrecioUnitario()));
              textNombre.setText(dpActual.getNombre());
//              textImagen.setText(dpActual.getImagen());
              textTipo.setText("Individual");
         } else {
              textNombre.setEnabled(true);
              textDescripcion.setEnabled(true);
              textDescuento.setEnabled(true);
//              textImagen.setEnabled(true);
              textPrecio.setEnabled(false);
              comboBoxAct.setEnabled(true);

              if (dpActual.getAcivada()) {
                   comboBoxAct.setSelectedIndex(1);
              } else {
                   comboBoxAct.setSelectedIndex(0);
              }
              textPrecio.setText("");
              textNombre.setText(dpActual.getNombre());
              textDescripcion.setText(dpActual.getDescripcion());
              textDescuento.setText(Integer.toString(dpActual.getDescuento()));
              textNombre.setText(dpActual.getNombre());
              textTipo.setText("Promocion");
//              textImagen.setText(dpActual.getImagen());
         }
    }//GEN-LAST:event_tablaProdsMouseClicked

