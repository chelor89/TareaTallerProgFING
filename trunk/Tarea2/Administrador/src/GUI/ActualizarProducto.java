    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
         dpActual = cr.seleccionarProducto((String) tablaProds.getValueAt(tablaProds.getSelectedRow(), 0),
                 (String) tablaProds.getValueAt(tablaProds.getSelectedRow(), 2));
         cr.seleccionarRestaurante(dpActual.getNickRes());
          
         if (!cr.existeProductoEnRestaurante(textNombre.getText()) || textNombre.getText().equals(dpActual.getNombre())) {
              if (dpActual.getTipo() == Producto.Individual) {
                   cr.actualizarIndividual(textNombre.getText(), textDescripcion.getText(),null /*textImagen.getText()*/,
                           Integer.parseInt(textPrecio.getText()));
              } else {
                   boolean v = comboBoxAct.getSelectedIndex() != 0;
                   cr.actualizarPromocion(textNombre.getText(), textDescripcion.getText(), null/*textImagen.getText()*/,
                           v, Integer.parseInt(textDescuento.getText()));
              }
              JOptionPane.showMessageDialog(null, "El producto ha sido actualizado con exito");
              tabla = (DefaultTableModel) tablaProds.getModel();
              tabla.setRowCount(0);
              Set<DataProducto> productos = cr.listarProductos();
              for (DataProducto dp : productos) {
                   tabla.addRow(new Object[]{dp.getNombre(), dp.getNombreRes(), dp.getNickRes()});
              }
              textDescuento.setText("");
//              textImagen.setText("");
              textDescripcion.setText("");
              textPrecio.setText("");
              textNombre.setText("");
              textTipo.setText("");
              comboBoxAct.setEnabled(false);
         } else {
              JOptionPane.showMessageDialog(null, "Ya existe un Producto con ese nombre en el Restaurante " + dpActual.getNombreRes());
         }
    }//GEN-LAST:event_jButton3ActionPerformed

